package cyjroxcode.copy;

public class Multiply {

	public static void main(String[] args) {
		System.out.println("Multiply by 3");

	for(int j = 3; j <= 12; ++j) {
		System.out.println(j * 3);
		}
	int counter = 0;
	
	while(counter <= 10) {
		++counter;
		System.out.printf(" %d", counter * 5);		
		}
	System.out.println();
	}

}
